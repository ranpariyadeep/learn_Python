#start from 0 to 5
for i in range(5):
    print("Current item 1:", i)

#1 to 10 with step of 2
#(start, stop, step)
#(inclusive, exclusive)
for i in range(1, 10, 2):
    print("Current item 2:", i)

#10 to 0 with step of -1
for i in range(10, 0, -1):
    print("Current item 3:", i)    

# 5 to 20 with step of 1
for i in range(5,20):    
    print("Current item 4:", i)  # This will not execute as the range is invalid